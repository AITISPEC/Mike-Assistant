        public static string Get(string key)
        {
            if (_responses.TryGetValue(key, out var list) && list.Count > 0)
                return list[_random.Next(list.Count)];
            return "Извините, я не понял.";
        }

        public static string GetForIntent(string intent, double weight)
        {
            if (!_responses.ContainsKey(intent))
                return Get("not_understand");

            var list = _responses[intent];
            int index = weight > 0.7 ? 0 : (weight > 0.4 ? 1 : 2);
            index = Math.Min(index, list.Count - 1);
            return list[index];
        }
